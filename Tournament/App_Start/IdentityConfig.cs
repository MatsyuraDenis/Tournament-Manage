    public class EmailService : IIdentityMessageService
    {
        public async Task SendAsync(IdentityMessage message)
        {
            // Подключите здесь службу электронной почты для отправки сообщения электронной почты.
            await ConfigSendGridAsync(message);
        }

        //var apiKey = Environment.GetEnvironmentVariable("riGMst8DQtueQOtYOT-nMQ");
        public async Task ConfigSendGridAsync(IdentityMessage message)
        {

            var apiKey = Environment.GetEnvironmentVariable("SENDGRID_KEY", EnvironmentVariableTarget.User);
            var client = new SendGridClient(apiKey);

            var msg = new SendGridMessage();
            msg.From = new EmailAddress(ConfigurationManager.AppSettings["Email"].ToString(), "TM");
            msg.Subject = message.Subject;
            msg.AddTo(message.Destination);
            msg.PlainTextContent = message.Body;
            msg.HtmlContent = message.Body;
            var response = await client.SendEmailAsync(msg);
        }
    }

